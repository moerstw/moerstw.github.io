彈幕射擊（STG）
起源於電子遊戲誕生的時代，是較早的遊戲類型。</p>

<p>遊戲中，玩家往往需要操作一個單位在躲避子彈和敵人的同時消滅敵人，以此通關。</p>

<p>街機時代是這一類型遊戲的黃金時代，當時的遊戲廳常常座無虛席，是街機三大類型遊戲之一。</p>

<p>到新的時代，這一類型隨著技術和遊戲業的不斷發展，最終這一類的遊戲越來越少。</p>

<p>在各大公司紛紛拋棄這一類型的同時，STG因其易製作耐玩度高的特點逐漸在自制遊戲界和同人遊戲界重新興起。
</p>
