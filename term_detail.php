<p>
東方project系列特有的系統，一種儀式化的戰鬥
<p>符咒規則就是相互競技，展現美感的決鬥規則。簡略地說就是—— 　</p>

<p>‧符咒（Spell Card）就是給自己的絕技加上名字。使用時要進行宣言。 </p>
<p>‧雙方在決鬥前要說清楚自己準備出多少張符。 　　</p>

<p>‧持有的符全部被擊破就代表那方輸了。 　　</p>

<p>‧勝利者只能拿決鬥前說好的報酬，如果對對手提出的報酬不滿意，可以拒絕決鬥。 　　</p>

<p>‧勝利者要積極接受失敗者的再次挑戰。 　　</p>

<p>‧要有覺悟，隨時可能出意外。 　　</p>

<p>就是這樣的內容，感覺更像是運動式的決鬥。 導入這規則後，力量脆弱的人類也有可能戰勝妖怪了。同時妖怪們也有可能打倒懶散的巫女，所以沒什麼妖怪會反對。</p>
</p>
