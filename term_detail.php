<p>
所謂自機狙，就是跟據自機所在位置所發出的狙擊彈幕。
主要分為奇數自機狙和偶數自機狙。
簡單地說，
奇數自機狙就是如果自機不移動就一定會被打中的彈幕，
偶數自機狙則是自機不移動就一定不會被打中的彈幕。
不過要小心，有些彈幕可能同時包含了偶數自機狙和奇數自機狙。 
</p>
